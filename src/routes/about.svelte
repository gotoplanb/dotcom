<h1>About this site</h1>

<p><strong>Cookbooks</strong> are tutorials for important things I do infrequently. Things like setting up a Heroku pipeline or authorizing the Salesforce CLI. I only need to do this stuff once per project, so it's easy to forget some of the steps with a few months in between uses. Cookbooks tell you <em>what</em> to do and not necessarily why.</p>

<p><strong>Crossovers</strong> are modifications to existing projects or tutorials. For example, I may take a Salesforce Trailhead project and then adapt to practice an additional skill. Crossovers inspire <em>how</em> you incorporate learning into your existing projects.</p>

<p><strong>Walkthroughs</strong> are step-by-step explainers to provide more context to existing content. Walkthoughts help you know <em>why</em> and <em>when</em> you might make certain decisions. </p>